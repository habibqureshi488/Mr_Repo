$(document).ready (function(){
    $("#btn1").click(function(){
        $("#b1").fadeIn();
        $("#b2").fadeIn("slow");
        $("#b3").fadeIn(3000);
    });
     $("#btn2").click(function(){
        $("#b1").fadeOut();
        $("#b2").fadeOut("slow");
        $("#b3").fadeOut(3000);
    });
     $("#btn3").click(function(){
        $("#b1").fadeToggle();
        $("#b2").fadeToggle("slow");
        $("#b3").fadeToggle(3000);
    });
     $("#btn4").click(function(){
        $("#b1").fadeTo("slow",0.25);
        $("#b2").fadeTo("slow",0.25);
        $("#b3").fadeTo(3000,0.25);
    });
     $("#btn5").click(function(){
        $("#b1").fadeTo("slow",1);
        $("#b2").fadeTo("slow",1);
        $("#b3").fadeTo(3000,1);
    });
    });